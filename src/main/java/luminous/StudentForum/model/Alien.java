package luminous.StudentForum.model;

public class Alien {
    private int aid;
    private String aname;
    private String lang;

    public void setAid(int aid) {
        this.aid = aid;
    }
    public int getAid() {
        return aid;
    }
    public void setAname(String aname) {
        this.aname = aname;
    }
    public String getAname() {
        return aname;
    }
    public void setLang(String lang) {
        this.lang = lang;
    }
    public String getLang() {
        return lang;
    }
}
